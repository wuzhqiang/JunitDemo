package com.cfet.junit;
/**
  * 计算机器基本功能加减乘除
 * @author 伍志强
 * @date 2019/12/24
 */
public class Calculator {
  /**
      *   加法运算
   * @param one
   * @param two
   * @return 
   */
  public static int add(int one ,int two) {
	  return one + two;
  }
  /**
       * 减法运算
   * @param one
   * @param two
   * @return
   */
  public static int subtract(int one ,int two) {
	  return one-two;
  }
  /**
       *  除法运算
   * @param one
   * @param two
   * @return
   */
  public  static int divide(int one ,int two) {
	  return one/two;
  }
}
